export const getRecipeById = (id: string): Recipe | undefined => {
  return recipes.find(recipe => recipe.id === id);
};

export const getRecipesByCategory = (category: Recipe['category']): Recipe[] => {
  return recipes.filter(recipe => recipe.category === category);
};

export const getRecipesByDietaryTag = (tag: Recipe['dietaryTags'][number]): Recipe[] => {
  return recipes.filter(recipe => recipe.dietaryTags.includes(tag));
};

export const searchRecipes = (query: string): Recipe[] => {
  const lowerQuery = query.toLowerCase();
  return recipes.filter(recipe =>
    recipe.name.toLowerCase().includes(lowerQuery) ||
    recipe.description.toLowerCase().includes(lowerQuery) ||
    recipe.ingredients.some(ing => ing.name.toLowerCase().includes(lowerQuery))
  );
};

